    private void limpar() {
        txtNomeMunicipio.setEnabled(true);
        txtSiglaEstado.setEnabled(true);
        rbClimaEquatorial.setEnabled(true);
        rbClimaSubtropical.setEnabled(true);
        rbClimaTropical.setEnabled(true);
        rbClimaSemiarido.setEnabled(true);
        txtGastos.setEnabled(true);        
        txtNomeMunicipio.setText("");
        txtSiglaEstado.setText("");
        txtGastos.setText("");
        txtNomeMunicipio.requestFocus();
    }

    public void Listar() {
        try {
            municipioDAO = new MunicipioDAO();
            List<Municipio> listarMunicipios = municipioDAO.listarMunicipios();
            DefaultTableModel modelo = (DefaultTableModel) tabelaMunicipios.getModel();
            modelo.setNumRows(0);
            for (Municipio mu : listarMunicipios) {
                modelo.addRow(new Object[]{
                    mu.getNome(),
                    mu.getFk_id_estado(),
                    mu.getPopulacao(),
                    mu.getClima(),
                    mu.getGastos(),});
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao Listar os municipios");
        }
    }

