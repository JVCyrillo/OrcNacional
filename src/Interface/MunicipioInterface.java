    private void InserirJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InserirJBActionPerformed
        try {
            municipio = new Municipio();
            municipio.setNome(txtNomeMunicipio.getText());
            municipio.setFk_id_estado(txtSiglaEstado.getText());
            if (rbClimaEquatorial.isSelected()) {
                municipio.setClima("Equatorial");
            } else if (rbClimaTropical.isSelected()) {
                municipio.setClima("Tropical");
            } else if (rbClimaSubtropical.isSelected()) {
                municipio.setClima("Subtropical");
            } else if (rbClimaSemiarido.isSelected()) {
                municipio.setClima("Semi√°rido");
            }
            float gasto = Float.parseFloat(txtGastos.getText());
            municipio.setGastos(gasto);
            municipioDAO = new MunicipioDAO();
            municipioDAO.cadastrarMunicipio(municipio);           
            limpar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar o municipio!");
            System.err.println(e);
        }
    }//GEN-LAST:event_InserirJBActionPerformed

    private void ExcluirJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirJBActionPerformed
        try {
            String nome = txtNomeMunicipio.getText();
            municipioDAO = new MunicipioDAO();
            municipioDAO.excluirMunicipio(nome);
            JOptionPane.showMessageDialog(null, "Municipio Excluido Com Sucesso!!!");
            limpar();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao Excluir o municipio");
        }

    }//GEN-LAST:event_ExcluirJBActionPerformed

    private void tabelaMunicipiosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMunicipiosMouseClicked
        txtNomeMunicipio.setText(tabelaMunicipios.getValueAt(tabelaMunicipios.getSelectedRow(), 0).toString());
        txtSiglaEstado.setText(tabelaMunicipios.getValueAt(tabelaMunicipios.getSelectedRow(), 1).toString());
        txtPupolacao.setText(tabelaMunicipios.getValueAt(tabelaMunicipios.getSelectedRow(), 2).toString());
        String clima = tabelaMunicipios.getValueAt(tabelaMunicipios.getSelectedRow(), 3).toString();
        if (clima.equals("Equatorial")) {
            rbClimaEquatorial.setSelected(true);
        } else if (clima.equals("Tropical")) {
            rbClimaTropical.setSelected(true);
        } else if (clima.equals("Subtropical")) {
            rbClimaSubtropical.setSelected(true);
        } else if (clima.equals("Semi√°rido")) {
            rbClimaSemiarido.setSelected(true);
        }
        txtGastos.setText(tabelaMunicipios.getValueAt(tabelaMunicipios.getSelectedRow(), 4).toString());
        txtNomeMunicipio.setEditable(false);
        txtPupolacao.setEnabled(false);
        txtSiglaEstado.setEnabled(false);
        rbClimaEquatorial.setEnabled(false);
        rbClimaTropical.setEnabled(false);
        rbClimaSubtropical.setEnabled(false);
        rbClimaSemiarido.setEnabled(false);
               
        
    }//GEN-LAST:event_tabelaMunicipiosMouseClicked

