    public void MunicipiosGGerais() {
        try {
            
            municipioDAO = new MunicipioDAO();
            List<Municipio> listarMunicipios = municipioDAO.listarMunicipios();
            DefaultTableModel modelo = (DefaultTableModel) tabelaMunicipiosClima.getModel();
            modelo.setNumRows(0);
            for (Municipio mu : listarMunicipios) {
                modelo.addRow(new Object[]{
                    mu.getNome(),
                    mu.getFk_id_estado(),
                    mu.getPopulacao(),
                    mu.getClima(),
                    mu.getGastos(),});
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao Listar os municipios");
        }
    }
    
    public void BuscaClima() {
        try {
            String clima = "";
            if (rbClimaEquatorial.isSelected()) {
                clima = "Equatorial";
            } else if (rbClimaTropical.isSelected()) {
                clima = "Tropical";;
            } else if (rbClimaSubtropical.isSelected()) {
                clima = "Subtropical";
            } else if (rbClimaSemiarido.isSelected()) {
                clima = "Semi√°rido";
            }
            municipioDAO = new MunicipioDAO();
            List<Municipio> listarMunicipios = municipioDAO.listarMunicipiosClima(clima);
            DefaultTableModel modelo = (DefaultTableModel) tabelaMunicipiosClima.getModel();
            modelo.setNumRows(0);
            for (Municipio mu : listarMunicipios) {
                modelo.addRow(new Object[]{
                    mu.getNome(),
                    mu.getFk_id_estado(),
                    mu.getPopulacao(),
                    mu.getClima(),
                    mu.getGastos(),});
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao Listar os municipios");
        }
    }

    

