    private void jBAdicionarGastoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAdicionarGastoActionPerformed
        try {
            String sigla = txtSiglaEstado.getText();
            String nome = txtNomeMunicipio.getText();
            float gastoAdicional = Float.parseFloat(txtGastoAdicional.getText());
            municipioDAO = new MunicipioDAO();
            municipioDAO.AdicionarGasto(sigla, nome, gastoAdicional);
            JOptionPane.showMessageDialog(null, "Valor inserido com sucesso com sucesso!");
            limpar();
            ((DefaultTableModel) tabelaMunicipios.getModel()).removeRow(0);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao insirir valor no municipio!");
            System.err.println(e);
        }


    }//GEN-LAST:event_jBAdicionarGastoActionPerformed

